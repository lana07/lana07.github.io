                <p>Girls' Generation, also known as SNSD, is a South Korean girl group formed by S.M. Entertainment. The group is composed of eight members: <a href="taeyeon2.php" class="cool-link">Taeyeon</a>, <a href="sunny2.php" class="cool-link">Sunny</a>, <a href="tiffany2.php" class="cool-link">Tiffany</a>, <a href="hyoyeon2.php" class="cool-link">Hyoyeon</a>, <a href="yuri2.php" class="cool-link">Yuri</a>, <a href="sooyoung2.php" class="cool-link">Sooyoung</a>, <a href="yoona2.php" class="cool-link">Yoona</a> and <a href="seohyun2.php" class="cool-link">Seohyun</a>. Originally a nine-piece group, <a href="jessica2.php" class="cool-link">Jessica</a> later departed from the group in September 2014. Girls' Generation debuted in 2007 with their Korean eponymous debut album. Though the album gained some attention, it was not until 2009 that the group rose to fame with the single "Gee", which claimed the top spot on KBS's Music Bank for a record-breaking nine consecutive weeks and was named the most popular song of the 2000s in South Korea by Melon. Girls' Generation further consolidated their popularity on the South Korean music scene with follow-up singles "Tell Me Your Wish (Genie)", "Oh!", and "Run Devil Run", which were released in mid-2009 and early 2010.</p>
