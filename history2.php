            <p>In mid-2010, Girls' Generation signed with Nayutawave Records (present-day EMI Records Japan) to venture into the Japanese music scene. Their 2011 eponymous Japanese debut album peaked atop the Japanese Oricon Albums Chart and became the first album by a non-Japanese girl group to be certified "Million" by the Recording Industry Association of Japan. The group's third Korean studio album The Boys was released in October 2011 and became the best-selling album of 2011 in South Korea with sales of over 380,000 copies. An English version of the single "The Boys" was released by Interscope Records in an attempt to expand the group's endeavor to the global music scene. The group's 2013 fourth Korean studio album I Got a Boy was supported by the single "I Got a Boy", which was subjected to major attention from Western media following its winning the Video of the Year award at the inaugural YouTube Music Awards. Their fifth Korean studio album, Lion Heart, was released in 2015. In 2017, Girls' Generation released their sixth Korean studio album, Holiday Night, to commemorate the group's tenth debut anniversary.
